bool sendData(Measurement measurement, const char *serverName, String board_token, StaticJsonDocument<300> *doc)
{
  String payload = "{}";
  HTTPClient http;
  const String userAgent = "Neuralume-Nebula";
  // Your Domain name with URL path or IP address with path
  //http.begin(serverName, root_ca); --> For production
  http.begin(serverName);
  http.setUserAgent(userAgent);
  // Specify content-type header
  http.addHeader("Content-Type", "application/json");
  http.addHeader("AUTHORIZATION", board_token);
  // Data to send with HTTP POST
  String httpRequestData = "{\"CO2\":" + String(measurement.co2) + ",\"T\":" + String(measurement.temperature) + ",\"RH\":" + String(measurement.humidity) + "}";
  // Send HTTP POST request
  int httpResponseCode = http.POST(httpRequestData);
  Serial.print("HTTP Response code: ");
  Serial.println(httpResponseCode);

  if (httpResponseCode == 201)
  {

    payload = http.getString();
    Serial.println(payload);
    http.end();
    DeserializationError err = deserializeJson(*doc, payload);
    if (err)
    {
      Serial.print(F("deserializeJson() failed: "));
      Serial.println(err.c_str());
      return false;
    }
    return true;
  }

  http.end();
  // Free resources
  return false;
}
